package org.apache.logging.log4j.catalog.jpa.model;

import java.io.Serializable;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.Table;

/**
 * A Catalog Attribute Constraint.
 */
@Entity
@Table(name = "ATTRIBUTE_CONSTRAINT")
public class ConstraintModel implements Serializable {

    private static final long serialVersionUID = 6836453963830996541L;

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Column(name = "ID")
    private Long id;

    @ManyToOne
    @JoinColumn(name = "ATTRIBUTE_ID")
    private AttributeModel attribute;

    @Column(name = "CONSTRAINT_TYPE")
    private String constraintType;

    @Column(name = "VALUE")
    private String value;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getConstraintType() {
        return constraintType;
    }

    public void setConstraintType(String constraintType) {
        this.constraintType = constraintType;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public AttributeModel getAttribute() {
        return attribute;
    }

    public void setAttribute(AttributeModel attribute) {
        this.attribute = attribute;
        if (attribute.getConstraints() != null) {
            attribute.getConstraints().add(this);
        }
    }
}
